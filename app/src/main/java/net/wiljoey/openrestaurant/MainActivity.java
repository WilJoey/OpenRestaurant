    private void requstData() {
        final TextView mTextView = (TextView) findViewById(R.id.txtHello);

        RequestQueue queue = Volley.newRequestQueue(this);
        //String url ="http://wiljoey.github.io/js/od.json";
        String url ="http://demo2.geo.com.tw/tpwo/scripts/od.js";
        // Request a string response from the provided URL.
        StringRequest stringRequest = new StringRequest(Request.Method.GET, url,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                        Gson gson = new Gson();
                        Type listType = new TypeToken<ArrayList<restaurant>>() {}.getType();


                        try {
                            String utf8String = new String(response.getBytes("ISO-8859-1"), "UTF-8");
                            ArrayList<restaurant> jsonArr = gson.fromJson(utf8String, listType);

                            mTextView.setText("Response is: "+ jsonArr.size()+";"+jsonArr.get(1).getDescription());
                        } catch (UnsupportedEncodingException e) {
                            e.printStackTrace();
                        }


                    }
                }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                mTextView.setText("That didn't work!");
            }
        });
        // Add the request to the RequestQueue.
        queue.add(stringRequest);
    }

